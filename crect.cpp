#include "crect.h"

////////////////////////////////////////////////////////////////////////
//
//	CRect()
//
//	Creator intalizes the member variables.
//
////////////////////////////////////////////////////////////////////////

CRect::CRect()
{
	mTop = mBottom = mLeft = mRight = 0;
};

////////////////////////////////////////////////////////////////////////
//
//	CRect()
//
//	Creator intalizes the member variables.
//
////////////////////////////////////////////////////////////////////////

CRect::CRect(int left,int top,int right,int bottom)
{
	Set(left,top,right,bottom);
};

////////////////////////////////////////////////////////////////////////
//
//	Set()
//
//	Copies values from another CRect.
//
////////////////////////////////////////////////////////////////////////

void CRect::Set( CRect* src )
{
	mTop = src->mTop;
	mLeft = src->mLeft;
	mBottom = src->mBottom;
	mRight = src->mRight;	
}

////////////////////////////////////////////////////////////////////////
//
//	CenterOn()
//
////////////////////////////////////////////////////////////////////////

void CRect::CenterOn( CRect* src, int w, int h )
{
	int x = (src->mLeft+src->mRight)/2;
	int y = (src->mTop+src->mBottom)/2;
	mTop = y - h/2;
	mLeft = x - w/2;
	mBottom = y + h/2;
	mRight = x + w/2;	
}

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

void CRect::Set( int left,int top,int right,int bottom )
{
	mTop = top;
	mLeft = left;
	mBottom = bottom;
	mRight = right;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

int CRect::Width( void )
{
	return mRight-mLeft;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

int CRect::Height( void )
{
	return mBottom-mTop;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

int CRect::XCenter( void )
{
	return (mRight+mLeft)/2;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

int CRect::YCenter( void )
{
	return (mBottom+mTop)/2;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

bool CRect::PointIn( int x, int y )
{
	if( x < mLeft ) return false;
	if( x > mRight ) return false;
	if( y < mTop ) return false;
	if( y > mBottom ) return false;
	return true;
};

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

void CRect::OffsetRect( int x, int y )
{
	mLeft+=x;
	mRight+=x;
	mTop+=y;
	mBottom+=y;
}

////////////////////////////////////////////////////////////////////////
//
//
//
////////////////////////////////////////////////////////////////////////

void CRect::DeflateRect(int x, int y )
{
	mLeft+=x;
	mRight-=x;
	mTop+=y;
	mBottom-=y;
}
